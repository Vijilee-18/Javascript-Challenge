//Basic Variable and Print Statement

//Using 'var' Keyword

var Name="Vijilee George Kurian";  //Overriding of Value and Variable is Possible.
console.log(Name); //printing statement 

//Using 'let' Keyword

let age=18; 
console.log(age);        //Overriding of Value is Possible but not Variable Name.

//using 'const' Keyword

const country="India";
console.log(country);    //Overriding of Value and Variable Name is Not Possible.